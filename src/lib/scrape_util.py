def download_image(img_url, img_id, root_folder):
    img_file = os.path.join(root_folder, img_id+'.jpg')
    if not os.path.exists(img_file):
        cover_pic = requests.get(img_url, timeout=15)
        with open(img_file, 'wb') as f:
            f.write(cover_pic.content)


def assemble_headers(headers, cookie=None):
    if headers is None:
        headers = dict()
    ua_info = get_random_ua()
    header_info = {
        'User-Agent': ua_info,
    }
    header_info.update(**headers)
    headers = header_info
    if cookie:
        headers['Cookie'] = cookie
    return headers


def get_random_ua():
    """
    获取随机ua
    """
    return random.choice(USER_AGENTS)


def vpn_connection_is_ok():
    """
    检查vpn网络连接
    """
    try:
        response = requests.get('https://www.google.com/', timeout=5)
        if response.status_code == 200:
            return True
    except Exception as e:
        logger.error(f'墙外网络不可用，请检查网络连接！网络错误内容：{e}')
        return False


